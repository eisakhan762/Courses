# Rules Of Defining A Variable Name
# 01. A Variable Name can contain alphabet, digits and underscores.
# 02. A Variable Name Can Start Only With Alphabet Or Underscore
# 03. A Variable Name can't start with a digit
# 04. No White space is allowed to be used inside a variable name

a = '''I am Hayat Khan King\r\nand I Live In Dariabad, Fatehput\r\nMy Father's Name Is Haseen Uddin Khan'''
b = 786
c = 346.13232
d = 'd'
e = False
f = None

# printing the variable
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)

# Printing the variable type
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
