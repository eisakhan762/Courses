# escape sequence char is python
# 01. \n for new line
# 02. \t for Tab
# 03. \' for Single quote
# 04. \" for Double quote
# 04. \\ for for one backslaish

print("Eisa is good boy\n He is very smart") # using \n
print("Eisa is good boy\tHe is very smart") # using \t
print('Eisa\'s father name is Haseen uddin khan') # using \'
print("Eisa\'s father name is \"Haseen uddin khan\"") # using \"
print("Mobile\\Smartphone") # using \\